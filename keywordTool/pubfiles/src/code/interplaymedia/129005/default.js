integration.on('adCallResult', function(e) {
    if (e.data.hasSkin) {
        $('body').addClass('inskinLoaded');
        var stylesCSS = '<style type="text/css">';
        stylesCSS += '.inskinLoaded .logo > img, .inskinLoaded .column-6 {width: 100% !important}';
        stylesCSS += '.inskinLoaded .column-11 > a {margin-right: 1px !important; margin-left: 0px !important}';
        stylesCSS += '.inskinLoaded .column-11 > .button {padding-right: 12px !important; padding-left: 12px !important}';
        stylesCSS += '</style>'
        $('head').append(stylesCSS);
    }

    if (e.data.format == 'Pagelead' || e.data.format == 'Pagelead Video') {
        integration.base.setCfg({'plr_FixedTop' : true, 'plr_EnableSwipe' : true});
    }
});

integration.on('adClose', function(e) {
    $('body').removeClass('inskinLoaded');
});
