integration.on('adCallResult', function(e) {
    if (e.data.hasSkin) {
        $('html').addClass('inskinLoaded');
        var stylesCSS = '<style type="text/css">';
        stylesCSS += '.inskinLoaded body{width: fit-content}';
        stylesCSS += '.inskinLoaded .home, .container{max-width: 375px}';
        stylesCSS += '.inskinLoaded .post-box{width: 375px !important; height: 270px !important}';
        stylesCSS += '.inskinLoaded .header_top{display: none}';
        stylesCSS += '</style>'
        $('head').append(stylesCSS);
    }

    if (e.data.format == 'Pagelead' || e.data.format == 'Pagelead Video') {
        integration.base.setCfg({'plr_FixedTop' : true, 'plr_EnableSwipe' : true});
    }
});

integration.on('adClose', function(e) {
    $('html').removeClass('inskinLoaded');
});